package ch03;

//null: 값이 없거나 정해지지 않은 상태의 값 
public class LiteralExampleNull {
	
	public static void main(String[] args) {
		//null 리터럴은 기본 타입에는 사용불가
		//int n=null;
		
		//객체 타입에 사용 가능 
		String str=null;
		Object obj=null;
		
		System.out.println(str);
		System.out.println(obj);
		
	}

}
